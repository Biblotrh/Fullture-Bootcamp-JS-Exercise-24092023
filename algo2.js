// 2. Prompt the user to enter a number.
let number = parseInt(prompt("Enter a number:"));

// 3. Check if the number is divisible by 2 (i.e., number % 2 equals 0):
if (number % 2 === 0) {
  // 4a. If it is divisible by 2, display "The number is even."
  console.log("The number is even.");
} else {
  // 4b. If it is not divisible by 2, display "The number is odd."
  console.log("The number is odd.");
}

